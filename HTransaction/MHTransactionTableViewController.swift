    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
       
        let hTransaction = hTransactions[indexPath.row]
        if let cell = tableView.dequeueReusableCellWithIdentifier("HTransactionCellTableViewCell", forIndexPath: indexPath) as? MHTransactionTableViewCell {
            cell.configureCell(hTransaction)
            return cell
        }
        return MHTransactionTableViewCell()
    }

